package com.patterns.program;

import java.util.Scanner;

public class Pattern1 {
public static void main(String[] args) {
	int n,r;
	Scanner sc = new Scanner(System.in);
	System.out.println("Enter the number: ");
	n=sc.nextInt();
	for (r = 1;  r<=n; r++) {
		System.out.print("* ");
		
	}
}
}
